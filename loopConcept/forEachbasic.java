package loopConcept;

public class forEachbasic {
    //An example of Java for-each loop  
 
    public static void main(String args[]){  
     //declaring an array  
     int arr[]={12,13,14,44};  
     //traversing the array with for-each loop  
     for(int i:arr){  
       System.out.println(i);  
     }  
   }   
  }  
    

