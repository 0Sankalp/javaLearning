
class Switch{
    public static void main(String[] args)
    {
      char a = 'C';
      switch (a)
      {
        case 'A':
          System.out.println("Letter A");
          break;
        case 'C':
          System.out.println("Letter C");
          break;
        default:
          System.out.println("Default case: NO Letter Matched!");
      }
    }
  }
  